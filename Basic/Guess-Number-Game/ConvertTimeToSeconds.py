# Take user input for the number of hours
Hours = int(input("Enter the hours : "))

# Take user input for the number of minutes
Minutes = int(input("Enter the minutes : "))

# Take user input for the number of seconds
Seconds = int(input("Enter the seconds : "))

# Convert hours, minutes, and seconds into total seconds
Result = (Hours * 3600) + (Minutes * 60) + Seconds

# Print the result in seconds
print(f"Result : {Result} seconds ")



